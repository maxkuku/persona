<p>В Клинике доктора Длина применяют самые современные и эффективные методики, которые помогают устранить болевой синдром и вернуть прежнюю подвижность пациенту:



<ul>
<li>Ди-Тазин терапия – авторская трехкомпонентная методика, в которой сочетаются мануальная терапия, фотодинамическая лазеротерапия и электрофорез. Комплексное воздействие позволяет в короткие сроки устранить болевой синдром, активизировать циркуляцию крови, нормализовать обменные процессы, остановить воспаление, предотвратить разрушение суставной ткани.
<li>Мануальная терапия – доказанный способ ручного воздействия, который помогает справиться со многими болезнями опорно-двигательного аппарата, снимает болевые ощущения, возвращает способность двигаться без ограничений.
<li>Общеукрепляющий массаж – ручная техника, которая улучшает кровоснабжение и питание клеток, снимает мышечное напряжение и повышает устойчивость организма в период нагрузок и напряжения. 
<li>Остеопатия – массажные действия направлены не только на позвоночник и мышцы, но и на внутренние органы для комплексного оздоровление и укрепления организма.
<li>Лечебно-физкультурный комплекс – проверенный метод лечения и профилактики суставных заболеваний, при котором уменьшается болевой синдром, улучшается кровоснабжение всех органов, правильно распределяется мышечная нагрузка. 
<li>Ударно-волновая терапия – восстановительная процедура, при которой используют низкочастотные акустические волны, ускоряет процесс заживления повреждённых тканей, уменьшает боли в спине при грыже и других заболеваниях позвоночника.
<li>Лекарственный электрофорез – воздействие на организм постоянным электротоком в сочетании с активными лекарственными веществами, обладает обезболивающими, противовоспалительными, регенерирующими, расслабляющими свойствами.
<li>Физиотерапия – направленная стимуляция организма с применением различных способов (воздействие воздухом, водными потоками, магнитным полем, лазерными лучами, ультразвуковым аппаратом). Нормализует мышечный тонус и метаболические процессы, улучшает кислородное питание тканей и органов.
</ul>





