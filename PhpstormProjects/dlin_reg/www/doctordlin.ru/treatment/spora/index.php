        <h1>Лечение пяточной шпоры </h1>

        <img width="500" hspace="9" vspace="9" align=right src="/bitrix/templates/dlin/images/stelka.jpg">


        <p>Строение стоп обусловлено их функциями – опорной, амортизирующей, балансировочной. Неудобная обувь, стоячая работа, перенапряжение сокращают природный запас прочности тканей. Такая нагрузка не проходит бесследно – со временем плантарные фасции изнашиваются, своды уплощаются, суставы теряют гибкость. Создаются условия для развития серьезных патологий, нарушающих качество жизни. К таким заболеваниям относится пяточная шпора, доставляющая значительные неудобства пациентам любого возраста.


        <h2>Почему появляется пяточная шпора? </h2>

        <p>Факторы, способствующие появлению пяточной шпоры:</p>

        <ul>
            <li>Лишний вес, увеличивающий нагрузку на стопы;
            <li>Гипертонус икроножных мышц;
            <li>Продольное плоскостопие;
            <li>Ходьба без обуви, длительное стояние;
            <li>Болезнь Бехтерева;
            <li>Нарушения кровообращения в ногах;
            <li>Травма стопы с затрагиванием пяточной кости;
            <li>Нарушения метаболизма (при сахарном диабете, подагре).

        </ul>

        <p>Основным «виновником» развития заболевания становится плоскостопие, потому что при расплющивании свода происходит неравномерное распределение давления на стопу. Компенсаторные механизмы не в силах справиться со столь серьезной нагрузкой, что приводит к перенапряжению и травматизации сухожилий. Закономерным результатом становится воспалительный процесс, охватывающий не только поверхностные, но и глубокие слои костной ткани.</p>



        <h2>Клиническая картина</h2>


        <p>Хроническое раздражение пяточной кости поддерживает воспаление, что обусловливает образование специфического костного выроста. Именно он получил название «шпоры» из-за крючкообразной формы. Вырост направлен внутрь мягких тканей стопы, и чем больше он увеличивается, тем более выраженный дискомфорт испытывает человек.
        <p>Как можно заподозрить наличие пяточной шпоры:
        </p>

        <ul>
            <li>Сильная боль при опоре на пятку, похожая на присутствие гвоздя в ноге;
            <li>Боль затихает в покое, далее беспокоит пациента даже без нагрузки;
            <li>Неприятные ощущения возникают после длительного нахождения без двигательной активности (например, после сна);
            <li>Изменение походки, обусловленное обереганием пятки от нагрузки и переносом центра тяжести на переднюю часть стопы.
            <li>Поперечное плоскостопие, при котором стопа распластывается, старая обувь начинает давить и натирать.

        </ul>

        <p>Диагноз подтверждается рентгенографическим исследованием стопы и УЗИ. Лечение пяточной шпоры должно быть начато своевременно, так как заболевание быстро прогрессирует и вызывает мучительные боли при неоказании помощи. Лечебные методы охватывают и терапевтические, и хирургические меры, однако не все способы отличаются быстротой и эффективностью воздействия. Именно поэтому УВТ приобрела особую популярность в ортопедии и травматологии, принося значительное облегчение уже после первых сеансов. Методика основана на воздействии звука определенной частоты в проекции костного разрастания, в большинстве случаев позволяет избавиться от пяточной шпоры без хирургического вмешательства.</p>


        <h2>Что такое ударно-волновая терапия?</h2>

        <p>Широкое распространение УВТ получила после того, как врачи оценили полезный эффект акустических волн, сопоставимый с интенсивным массажем. Контролируемое воздействие достигает места соединения пяточной кости и плантарной фасции, благодаря чему отложения солей начинают рассасываться. За несколько сеансов патологический нарост уменьшается или полностью исчезает. Таким образом, пациент получает положительный результат без разрезов, швов, дополнительных приспособлений и ограничения подвижности. Сглаживание пяточной кости можно увидеть на контрольной рентгенограмме после окончания курса лечения. Оптимальная программа лечения УВТ подбирается врачом с учетом запущенности заболевания, выраженности болевого синдрома, наличия сопутствующих патологий. Процедура проводится посредством специального прибора, генерирующего акустические волны нужной частоты. Специалист воздействует только на болевой очаг, здоровые ткани не затрагиваются. Количество сеансов назначается индивидуально (в среднем потребуется посетить врача до 5 раз). В нашем центре врач подбирает такую схему лечения, которая позволит получить эффект в кратчайшие сроки, избавляя от необходимости постоянного приема анальгетиков.</p>





