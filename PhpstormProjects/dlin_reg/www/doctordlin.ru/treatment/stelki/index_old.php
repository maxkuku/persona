<p>Деформации стоп, боли в суставах и мышцах – это издержки цивилизации, результат многолетней ходьбы человека по плоским искусственным покрытиям. Для того чтобы устранить эти проблемы, необходимо просто вернуть стопу человека в "природные" условия.
<p>Тысячи людей, взрослые и дети, профессиональные спортсмены и любители здорового образа жизни, носят индивидуальные ортопедические стельки каждый день.  Даже при использовании их здоровыми людьми снижается риск возникновения и развития патологий стопы и осанки, травматизма. Индивидуальные стельки обладают антистрессовыми свойствами.

<p><b>Подиатрия – достаточно молодое для нашей страны направление медицины. В зарубежных странах, таких как Великобритания, США, Канада, Австралия и Новая Зеландия подиатрия как отдельная дисциплина изучается и развивается уже давно. </b></p>

<p>Что же такое подиатрия? Это наука о проблемах стоп, нижних конечностей, таза и позвоночного столба, а также о взаимосвязях стопы и тела. Ведь, как известно, наше тело, от стоп до макушки – это отточенная эволюцией конструкция, где все звенья взаимосвязаны. Подиатрия сформировалась на стыке различных медицинских дисциплин и опирается на последние достижения клинической биомеханики, ортопедии, неврологии, остеопатии, спортивной медицины, прикладной кинезиологии, постурологии, терапии и хирургии опорно-двигательной системы человека. Подиатр – специалист по изготовлению индивидуальных супинаторов/ ортезов/ стелек. 



		<h2>Показания к применению индивидуальных стелек</h2>

<ul>
<li>дискомфорт, усталость (повышенная утомляемость мышц) и боли в стопах, нижних конечностях и спине при ходьбе и/или в покое;</li>
<li>излишнее давление и напряжение на подошву (натоптыши и сухие мозоли);</li>
<li>поперечно-продольное плоскостопие;</li>
<li>анатомические и функциональные укорочения нижней конечности и связанные с этим патологии: перекос/искривление таза, грыжи дисков и пр.;</li>
<li>сколиоз, сколиотическая деформация, нарушение осанки и походки;</li>
<li>косолапость, Х-образное и О-образное искривление ног;</li>
<li>пяточная "шпора" (подошвенный фасциит);</li>
<li>онемения и/или жжение в стопе (невринома Мортона);</li>
<li>тендовагинит ахиллова сухожилия;</li>
<li>деформации и боли в суставах (артрозы, артриты) нижних конечностей и др. отделах опорно-двигательного аппарата;</li>
