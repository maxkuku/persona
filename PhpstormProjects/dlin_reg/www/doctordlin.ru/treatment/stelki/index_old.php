<li>посттравматические и послеоперационные изменения суставов, связок, сухожилий, суставных сумок и мышц (боли, деформации, укорочения, контрактуры, ограничения движений);</li>
<li>парезы и параличи периферических нервов нижних конечностей;</li>
<li>нарушение окклюзии (прикуса);</li>
<li>профилактика профессиональных перегрузок при длительном стоянии, ходьбе и/или беге;</li>
