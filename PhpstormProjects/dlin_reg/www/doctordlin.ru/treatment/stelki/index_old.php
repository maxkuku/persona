<p>Долгое время в российской медицинской практике лечение проблем опорно-двигательного аппарата человека чаще всего сводилось к чисто симптоматической фармакотерапии болевых проявлений. А различные проблемы, локализованные за пределами стопы, до сих пор не все врачи связывают с индивидуальными особенностями строения и функции стоп. Однако только четкое понимание патогенеза проблемы является залогом эффективного лечения и профилактики. Практика показывает, что во многих случаях стойкие болевые синдромы скелетно-мышечной системы бесследно исчезают после грамотной коррекции функции стоп специальными мануальными приемами, упражнениями и продвинутыми ортопедическими стельками.
<p>Только комплексное применение суставных техник и регулярное ношение индивидуальных стелек способно восстановить оптимальный паттерн движения. Использование ортопедических стелек позволяет равномерно распределить нагрузку на все группы мышц, обеспечить равновесие в вертикальном положении и правильную походку. Это позволяет улучшить кровообращение, снизить мышечное утомление и  износ суставов. При использовании ортопедических стелек для обуви заметно улучшается осанка, и уменьшаются боли в спине!
<p>Можно купить стандартные стельки и надеяться, что они окажут помощь в лечении плоскостопия. Или использовать ортопедическую обувь и рассчитывать на то, что она удобна. Однако все эти меры не дают 100% результата.
<p>Со всей серьёзностью следует подойти и к выбору индивидуальных ортопедических стелек на заказ. Нельзя доверять здоровье своих ног жёстким ортезам или серийным вкладышам-супинаторам. Ведь наши стопы такие же уникальные, как и наши лица ( «нет одинаковых лиц – нет одинаковых стоп»).
