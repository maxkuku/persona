<p><img  width="400" hspace="9" vspace="9" align=right src="/bitrix/templates/dlin/images/gryzha-grudnogo-otdela.jpg">Позвоночник – это основа скелета и всего человеческого тела, он позволяет нам двигаться, наклоняться, бегать. В Клинике доктора Длина вы сможете пройти лечение межпозвонковой грыжи грудного отдела позвоночника без операций. Коррекция патологии проводится с помощью мануальной техники и уникальной авторской методики.
	<h2>Причины и симптомы межпозвонковой грыжи грудного отдела</h2>
<p>Более 60% взрослого населения испытывает определённые «проблемы со спиной», которые ограничивают подвижность, приводят к постоянному болевому синдрому и дисфункции внутренних органов. Множество причин вызывает жалобы пациентов на боли в груди, спине и других частях тела, ведь иннервация от позвоночного столба проникает практически во все органы. Такое положение вещей часто осложняет диагностику, затрудняет постановку верного диагноза. Межпозвонковую грыжу часто можно перепутать с другими болезнями. 
<p>Главные факторы риска, приводящие к заболеваниям позвоночника:
<ul><li>генетические, наследственные – анатомические отклонения, склонность к воспалениям в организме;
<li>производственные – тяжёлый труд, связанный с перегрузками, или длительное пребывание в неправильной позе (сидячая работа, переноска тяжестей);
<li>неврологические – нарушение иннервации и кровообращения в позвоночнике, стрессовое воздействие;
<li>травматические – давление и удары при спортивных занятиях или в повседневной жизни;
<li>повседневные – избыточный вес, неправильный образ жизни, последствия от продолжительного нарушения осанки.
</ul>
<p>Помимо этого боли в спине возникают по причине органических патологий, в числе которых:
<ul><li>остеохондроз – дистрофическое поражение хрящевой ткани;
<li>сколиоз, лордоз, кифоз – искривление позвоночника в различных отделах;
<li>спондилез, спондилоартроз – нарушение в процессе питания клеток межпозвоночного диска;
<li>позвоночный стеноз – частичное перекрытие спинномозгового канала;
<li>радикулит или люмбаго – воспаление седалищного нерва;
<li>нестабильное состояние позвонков, переломы позвоночника, повреждения связок.
</ul>
<p>Очень важно своевременно обращаться к специалистам. Болезни гораздо быстрее и проще вылечить на ранних стадиях, чем при запущенных формах. Следует обратить внимание на такие симптомы:
<ul><li>острые боли в спине или шее на протяжении 2-3 дней и дольше;
<li>хронические боли ощущаются даже в период применения обезболивающих или противовоспалительных препаратов;
<li>острый дискомфорт возникает резко, по непонятной причине;
<li>боли появляются после определённых нагрузок или упражнений;
<li>болевые симптомы в спине отдают в ногу, колено, ступню;
<li>покалывание, ощущение «мурашек», онемение в грудной клетке, спине, плечах и руках;
<li>отечность, покраснение, признаки воспалительного процесса;
<li>ограничение подвижности, невозможность полноценно существовать, выполнять привычные действия.
</ul>
	<h2>Виды и стадии межпозвонковой грыжи в грудном отделе</h2>
<p>В зависимости от размеров грыжи, которая перекрывает внутрипозвоночный канал, выделяют такие виды:
<ul><li>малые – сокращают размеры канала до 10%;
<li>средние – сужают канал на 10-20%;
<li>большие – перекрывают позвоночное пространство более, чем на 20%.
</ul>
<p>Выпячивание торакальной грыжи происходит постепенно за несколько стадий, на каждой из которых ощущаются свои симптомы. 
<ul><li>Протрузия – I стадия (начальная), при которой в ослабленную часть фиброзного кольца частично проникает ядро позвонкового диска. В результате возникает небольшое выбухание на 1-5 мм. Внутренние оболочки покрываются микротрещинками, но наружный слой находится в целостном состоянии. Это еще не грыжа, но уже происходят изменения в диске, он истончается и в любой момент может разорваться.
<li>Экструзия – II-III промежуточные стадии, когда нарушается целостность фиброзной оболочки и через получившийся разрыв в позвоночный канал проникает часть пульпозного вещества. На этом этапе выпяченная часть диска начинает раздражать спинномозговые нервы, вызывая неврологические симптомы. Размеры грыжи определяются в среднетяжелом случае до 5-8 мм, а в тяжелом более 8 мм.
<li>Секвестрация – IV последняя стадия, самое опасное состояние грыжи. Происходит разрыв, отделение отмерших грыжевых фрагментов от основной части пульпозного ядра. Отслоение некротизированных фрагментов приводит к заражению спинномозгового канала, что угрожает фатальными последствиями. Возможно повреждение нервов и спинного мозга. Далее – инвалидность, обездвиженность, утрата привычных органических функций. Это страшное состояние, которое в большинстве случаев можно предупредить, обратившись к врачам на ранней стадии развития изменений. 
</ul>

