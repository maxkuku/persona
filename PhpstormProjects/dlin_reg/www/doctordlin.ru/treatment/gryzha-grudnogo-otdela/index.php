<p>На бесплатной консультации в Клинике доктора Длина проводится тщательная диагностика всего позвоночного столба и отдельно каждого сегмента. В результате становится понятно, на каком участке задеты нервные окончания. На основании диагностических данных назначается индивидуальная программа лечения. После первого приёма человек может почувствовать облегчение и рассчитать удобный график дальнейшего лечения. 
<p>В клинике используются самые современные и эффективные методики, которые помогают убрать болевой синдром и вернуть прежнюю подвижность пациенту.
<ul><li>Лечебная блокада – избавление от острой боли при введении анальгезирующих препаратов непосредственно к нервным волокнам.
<li>Внутрисуставные уколы – инъекции с лечебным составом на основе хондропротекторов, которые выполняют прямо в область суставного сочленения. 
<li>Лекарственный электрофорез – воздействие на организм постоянным электротоком в сочетании с активными фармакологическими веществами.
<li>Ударно-волновая терапия – ускоренный способ заживления повреждённых тканей, уменьшает боли в спине при грыже и других заболеваниях позвоночника.
<li>Мануальная терапия – ручное воздействие на пораженные участки позвоночника и прилегающие области, чтобы устранить защемление и восстановить двигательные способности.
<li>Общеукрепляющий массаж – проверенный способ улучшить кровоснабжение, снять мышечное напряжение и повысить защитные свойства организма. 
<li>Остеопатия – безопасный метод воздействия на организм, при котором происходит комплексное оздоровление и полное восстановление всех органов и систем.

<li>Ди-Тазин терапия – авторская трехкомпонентная методика, в которой сочетаются приёмы мануальной терапии, фотодинамической лазеротерапии и электрофореза. Комплексное воздействие позволяет за 1-2 сеанса убрать болевой синдром, стимулировать микроциркуляцию крови и обмен веществ, прекратить воспаление и разрушение суставной ткани.
<li>Лечебно-физкультурный комплекс – проверенный метод лечения и восстановления организма, при котором уменьшается болевой синдром, возвращается прежняя подвижность.
<li>Физиотерапия – стимуляция организма, направленная на самовосстановление с применением различных способов (воздействие воздухом, водными потоками, магнитным полем, лазерными лучами, ультразвуковым аппаратом).
</ul>
<p>Всего в клинике применяют 25 безоперационных методик для лечения позвоночника и суставов. В штате работают врачи только высокой квалификации: вертебрологи, неврологи, остеопаты, мануальные терапевты. Они постоянно стажируются в лучших клиниках Израиля, Европы, Америки.
