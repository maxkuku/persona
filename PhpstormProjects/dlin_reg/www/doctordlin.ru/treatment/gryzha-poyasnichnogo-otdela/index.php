<p><img  width="400" hspace="9" vspace="9" align=right src="/bitrix/templates/dlin/images/gryzha-poyasnichnogo-otdela.jpg">Поясница получает колоссальную нагрузку ежедневно. Сотни сгибаний и разгибаний спины, переноска тяжестей, осложненный труд и обездвиженность ― все эти вещи ослабляют позвоночник, служат причиной развития нарушений. Грыжи в поясничном отделе позвоночника – занимают более 50% случаев в диагностике межпозвонковых грыж. В Клинике доктора Длина проводится лечение патологии без применения оперативного вмешательства. Мы используем мануальные техники, совмещая их с уникальными авторскими методиками.

<h2>Причины и симптомы межпозвонковой грыжи поясничного отдела</h2>



<p>Позвоночные диски поясничного отдела выполняют амортизирующие функции, позволяют туловищу нагибаться. Они поглощают и смягчает нагрузки на позвоночник в моменты физической активности, поддерживают необходимую гибкость и прочность скелетного каркаса. Межпозвонковая грыжа появляется по причине дегенеративно-дистрофических изменений в позвоночнике, которые затрагивают смежные позвонки. Дегенерация диска происходит по таким причинам: 


<ul>
<li>нарушения клеточного метаболизма;
<li>дефицит питания и кровоснабжения. 

</ul>






<p>Из-за этого межпозвонковые оболочки теряют влагу и становятся менее эластичными, постепенно истончаются, на фиброзном кольце образуются микротрещины. Туда перемещается пульпозное ядро, и диск выступает за анатомические ориентиры. Далее происходит разрыв фиброзной оболочки, через который студенистая масса проникает в спинномозговой канал.
<p>Чаще всего это последствие запущенного остеохондроза, из-за которого диски между позвонками сплющиваются и трутся друг об друга. При этом начинается воспаление, отечность, механическая компрессия нервных окончаний вызывает сильнейшие боли. Иннервация от позвоночного столба проникает почти во все органы и ткани, поэтому запущенная межпозвонковая грыжа приводит к тяжёлым последствиям: наступает ограничение подвижности или даже паралич, нарушаются функции внутренних структур.
<p>Постепенно симптоматика становится более выраженной:


<ul>
<li>появляются ситуативные или постоянные боли в поясничном отделе, по описаниям пациентов тупые, ноющие, колющие, «с прострелом», которые захватывают брюшину и грудную клетку;
<li>усиливаются болевые ощущения при нагрузках, поворотах, попытках сделать глубокий вдох;
<li>возникают всевозможные парестезии или онемения в спине, плечах, конечностях, чрезмерная напряженность спинных мышц;
<li>ограничивается амплитуда движений, происходит вынужденное изменение осанки и походки из-за болевых проявлений;
<li>проявляются односторонние ирридиирующие боли в ногу или другие части тела.

</ul>





<p>Иногда симптомы проявляются в общем ухудшении самочувствия, изменении артериального давления, зрительных и слуховых нарушениях. 
<p>В тяжёлых случаях при поражении мозга заметны системные дисфункции: депрессивное состояние, расстройство вестибулярного аппарата, изменения обычного режима жизни. Часто позвонковые грыжи осложняются такими болезнями: остеохондроз, сколиоз, лордоз, кифоз, спондилез, спондилоартроз, позвоночный стеноз, радикулит или люмбаго, травматические повреждения позвоночника, растяжения связок.


	<h2>Виды и стадии межпозвонковых грыж в пояснице</h2>



<p>В зависимости от размеров выпячивания, которое перекрывает внутрипозвоночный канал, выделяют такие виды грыжи: малые, средние и большие. В осложнённых случаях грыжа занимает до 20% позвоночного просвета. Различают грыжи по степени тяжести, в зависимости от патологических изменений:



<ul>
<li>I стадия – протрузия, начальное состояние с незначительным смещением диска до 4 мм. Фиброзное кольцо истончается, появляются микротрещины, однако сохраняется его целостность.
<li>II-III стадии – экструзия, когда окончательно формируется грыжа с размерами более 5-6 мм. Фиброзная оболочка разрывается, ядро провисает в межпозвонковом канале. 
<li>IV стадия – секвестрация, критический момент, в который от диска и ядра отрываются некротизированные фрагменты и попадают в позвоночный канал, что вызывает воспаление. Эта стадия может привести к параличу и тяжелым аутоиммунным реакциям.

</ul>


