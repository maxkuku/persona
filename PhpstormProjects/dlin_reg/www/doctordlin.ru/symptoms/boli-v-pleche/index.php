        <h1>Диагностика и лечение боли в плече</h1>


<p>Наш плечевой сустав уязвим. Он недолго выдерживает неправильное обращение и отвечает на него болью и нарушением функций. Причины боли в плечевом суставе разнообразны — тяжёлая физическая работа, усиленные тренировки в спортзале, вирусные заболевания. Плечо может болеть из-за ушиба, вывиха или перелома. Постоянная боль в левом или правом плече — повод обратиться к врачу. Это предотвратит развитие болезни и не даст ей перейти в хроническую форму.</p>

<p>Боль в плечевом суставе рискуют почувствовать люди, которые:</p>
<ul>
<li>сутулятся, неследят заосанкой;</li>
<li>мало двигаются в течение дня;</li>
<li>выполняют тяжёлую физическую работу;</li>
<li>профессионально занимаются спортом.</li>
</ul>
<h2>Типы болей в плече</h2>
<p>Плечи чаще всего начинают болеть из-за перегрузки. Усиленно занимаясь спортом, поднимая тяжести, мыпровоцируем появление боли. Она может быть симптомом различных заболеваний. Рассмотрим, какие болезни какой тип боли в плече вызывают.</p>

<h3>Острая боль</h3>
<p><strong>Остеохондроз шейного отдела позвоночника</strong>. Боли отдают вруку и усиливаются при повороте головы. Усиливаются по ночам и становятся нестерпимыми. В запущенных случаях у человека неподнимается рука.</p>
<p><strong>Артрит</strong>&mdash; воспаление сустава. При артрите сильно болит правое или левое плечо.</p>
<p><strong>Артроз</strong>&mdash; распространённая причина острой боли в плечевом суставе. Чаще встречается у пожилых людей. Человеку больно поднять руку ивыполнить другое движение. Артроз могут вызвать ушибы, вывихи, переломы, растяжения.</p>
<p><strong>Неврит плечевого нерва</strong>. Его вызывают воспалительные процессы, опухоли, травмы. Человек внезапно чувствует резкую боль в плечевом суставе.</p>
<p><strong>Тендобурсит</strong>&mdash; воспаления суставной сумки. Его вызывают отложения солей кальция. Для тендобурсита характерна острая боль вовсей руке отшеи докончиков пальцев. Больному трудно двигать рукой.</p>
<p><strong>Поражение вращающейся манжетки плеча</strong>. Болезнь вызывают нестандартные движения рукой&mdash; например, покраска потолка. Наследующий день после малярных работ человек может почувствовать острую боль в плече.</p>
<p><strong>Капсулит плечевого сустава</strong>. Болезнь вызывает скованность мышц плечевого пояса. У человека сильно болит правое или левое плечо, он неможет свободно поднять руку наверх, отвести в сторону или завести за спину.</p>
<p><strong>Травмы плеча</strong>&mdash; переломы, растяжения, вывихи. Травмы нужно лечить, иначе они приведут к стойким нарушениям функции плеча и станут хроническими.</p>

<h3>Хроническая боль</h3>
<p><strong>Тендинит</strong>. Это воспаление сухожилий, окружающих плечевой сустав. Тендинит обычно возникает из-за повышенной нагрузки. При тяжёлой физической работе сухожилия трутся о поверхность сустава. Это вызывает хроническую ноющую боль.</p>
<p><strong>Тендинит бицепса</strong>. Это воспаление сухожилия мышцы внутренней части плеча. У человека постоянно болит плечо. Боль усиливается при движении руки и прощупывании мышцы. При разрыве сухожилия бицепса появляется вздутие в виде шарика.</p>
<p><strong>Бурсит</strong>. Возникает при чрезмерной нагрузке на сустав, после травм околосуставной сумки или сухожилий. Человек испытывает резкую боль, к ней присоединяется отёк. На поражённом плече невозможно спать.</p>


<h3>Ноющая боль</h3>
<p><strong>Межпозвоночная грыжа шейного отдела</strong>. Вызывает непостоянную ноющую боль в плечевом суставе и шее. К ней присоединяются головные боли и головокружения. При развитии болезни ситуация ухудшается. Боль становится постоянной и нестерпимой.</p>
<p><strong>Плечелопаточный периартрит</strong>. Боль бывает жгучей, ноющей и простреливающей. Начинается постепенно, без очевидных причин. Часто &laquo;будит&raquo; по ночам, при этом мёрзнут руки. Движения причиняют боль в суставах. В запущенных случаях нарушаются функции кисти, немеют руки.</p>
<p><strong>Миалгия</strong>. Болезнь вызывают физические перегрузки, переохлаждение, вирусные инфекции. Человек чувствует ноющую, иногда острую боль в мышцах, не может двигать руками в полную силу.</p>



