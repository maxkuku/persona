            <li class="li1">Кортикостероиды. Эта группа быстро подавляет воспаление, снижает отечность и высвобождает нервные волокна &laquo;из плена&raquo; разбухших тканей;</li>
            <li class="li1">Лидаза. Фермент, расщепляющий разрастания соединительной ткани, которые появляются при хронических воспалениях;</li>
            <li class="li1">Витамины группы B, восстанавливающие функциональность нерва.</li>
        </ul>
