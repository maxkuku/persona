        <p class="p1">Технология паравертебрального введения была разработана еще советскими учеными. А поскольку главной целью было обезболивание, то и препараты использовались соответствующие &ndash; новокаин, лидокаин, ультракаин. Единственным недостатком их является непродолжительность действия: уже через 1-2 часа болезненность и скованность возвращаются вновь. Поэтому сейчас врачи чаще используют многокомпонентные смеси, способные оказать пролонгированный и терапевтический эффект.</p>
