        <p class="p1">Процедуру проводят в стерильных условиях, соблюдая принципы асептики и антисептики. Пациент располагается на кушетке. Врач методом прощупывания находит источник боли. Тонкой иглой проводят обезболивание кожи в 4 точках &ndash; по бокам от остистых отростков позвонка. Далее технология зависит от избранной методики блокады:</p>
        <ul class="ul1">
            <li class="li1">Инфильтрационная. Препарат рассеивается в мягких тканях вокруг источника боли. Анестетиком пропитываются кожа и мышцы;</li>
            <li class="li1">Проводниковая. Иглу вводят до упора в тело позвонка, немного оттягивают назад &ndash; таким образом находится точка выхода нервного корешка из позвоночника. Обволакивание его обезболивающим препаратом позволяет добиться устойчивого результата &ndash; пациент и не вспомнит о дискомфорте в ближайшие несколько часов;</li>
